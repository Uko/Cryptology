	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton1ActionPerformed
	{//GEN-HEADEREND:event_jButton1ActionPerformed
		try
		{
			int p = Integer.parseInt(pTextField.getText());
			int q = Integer.parseInt(qTextField.getText());
			if (GCD.euclidean(p, q) == 1)
			{
				nTextField.setText(new Integer(p * q).toString());
				phiTextField.setText(new Integer((p - 1) * (q - 1)).toString());
			}
		}
		catch (NumberFormatException e)
		{
		}
	}//GEN-LAST:event_jButton1ActionPerformed
	private void jButton2ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton2ActionPerformed
	{//GEN-HEADEREND:event_jButton2ActionPerformed
		try
		{
			int phi = Integer.parseInt(phiTextField.getText());
			int e = Integer.parseInt(eTextField.getText());
			if ((e > 1) && (e < phi) && (GCD.euclidean(phi, e) == 1))
			{
				int d = 0;
				while ((d * e) % phi != 1)
				{
					d++;
				}
				dTextField.setText(new Integer(d).toString());
			}
		}
		catch (NumberFormatException e)
		{
		}
	}//GEN-LAST:event_jButton2ActionPerformed
	private void jButton3ActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_jButton3ActionPerformed
	{//GEN-HEADEREND:event_jButton3ActionPerformed

		try
		{
			String text = srcTextArea.getText();
			int n = Integer.parseInt(n2TextField.getText());
			int ed = Integer.parseInt(edTextField.getText());
			char[] dst = new char[text.length() + 5];
			for (int i = 0; i < text.length(); i++)
			{
				dst[i] = (char) BinaryMethod.calculate((int) text.charAt(i), ed, n);
			}
			dstTextArea.setText(new String(dst));
		}
		catch (NumberFormatException e)
		{
		}
	}//GEN-LAST:event_jButton3ActionPerformed

